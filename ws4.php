            <section class="workshop genie-magic-wrapper">
                <h1>Genie Magic</h1>

                <div class="genie-description-wrapper">
                    <p>Genie Magic is an extension to ViSi-Genie, which is available in Workshop4 PRO. Genie Magic allows the User to add 4DGL code to various points in the ViSi-Genie backend code, which allows Users to take full advantage of the power of 4DGL, while retaining the speed and convenience of ViSi-Genie and its defined communication protocol.</p>

                    <p>Genie Magic allows you to enter 4DGL code in the form of Events, Code and Objects, along with various touch related additions too, giving you to the power to achieve advanced GUI's in ViSi-Genie without having to write the full code yourself.</p>

                    <p>Genie Magic is fully supported by the genieArduino and geniePi libraries, making communication with an Arduino or Raspberry Pi host even more powerful. </p>

                    <p>When using Genie Magic in combination with the Smart Widget Editor, it is possible to use the Smart Widgets Editor tool and Smart Widgets to create other graphical user interface elements such as: </p>
                </div>

                <ul>
                    <li>dropdown menu</li>
                    <li>side menu</li>
                    <li>'carousel slider'</li>
                    <li>list box</li>
                    <li>quarter-rotary menu</li>
                    <li>dock menu</li>
                    <li>fading slide-to-unlock button</li>
                    <li>spectrum with transparency</li>
                    <li>and many others</li>
                </ul>
            </section>

